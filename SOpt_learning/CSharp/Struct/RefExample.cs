
public readonly ref struct RefRoPoint4D {
    public readonly double X;
    public readonly double Y;
    public readonly double Z;
    public readonly double W;
    public RefRoPoint4D(double x, double y, double z, double w) {
        X = x;
        Y = y;
        Z = z;
        W = w;
    }
}

