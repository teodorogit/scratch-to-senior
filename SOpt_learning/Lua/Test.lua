function test (a)
    if a then print(type(a) .. " '" .. tostring(a) .. "'" .. " corresponde a verdadeiro")
    else print(type(a) .. " '" .. tostring(a) .. "'" .. " corresponde a falso") end
end

temp = test("masuia")
print(tmp) -- só para você ver o resultado intermediário.
test(tmp)

